function getTypeColor(type) {
    switch (type) {
        case 'grass':
            return '#63BB5B';
        case 'fire':
            return '#FF9C54';
        case 'water':
            return '#4D90D5';
        case 'poison':
            return '#AB6AC8';
        case 'flying':
            return '#92AADE';
        case 'bug':
            return '#90C12C';
        case 'normal':
            return '#9099A1';
        case 'electric':
            return '#F3D23B';
        case 'ground':
            return '#D97746';
        case 'psychic':
            return '#F97176';
        case 'rock':
            return '#C7B78B';
        case 'ghost':
            return '#5269AC';
        case 'fairy':
            return '#EC8FE6';
        case 'fighting':
            return '#CE4069';
        case 'ice':
            return '#74CEC0';
        case 'dragon':
            return '#096DC4';
        case 'dark':
            return '#5A5366';
        case 'steel':
            return '#5A8EA1';
    }
}

